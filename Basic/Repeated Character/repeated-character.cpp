class Solution
{
  public:
    char firstRep (string s)
    {
        //code here.
        unordered_map<int,int>m;
        for(auto i:s){
            m[i]++;
        }
        for(auto i:s){
            if(m[i]>1) return i;
        }
        return '#';
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string s; cin >> s;
        Solution ob;
        char res = ob.firstRep (s);
        if (res == '#') cout << -1 << endl;
        else cout << res << endl;
    }
    return 0;
}



