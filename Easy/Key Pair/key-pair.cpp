class Solution{
public:	
	// Function to check if array has 2 elements
	// whose sum is equal to the given value
	bool hasArrayTwoCandidates(int arr[], int n, int x) {
	    // code here
	    unordered_map<int,int>m;
	    for(int i=0;i<n;i++){
	        m[arr[i]]++;
	    }
	    int ans=0;
	    for(int i=0;i<n;i++){
	           ans=x-arr[i];
            m[arr[i]]--;
            if(m.find())
              return 1;
    }
    return 0;
	}
};

